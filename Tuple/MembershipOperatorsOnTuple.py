'''
Using membership operators in tuple
to check if an element is present
in a tuple or not.
'''

my_tuple = (1, 2.0, 'c', "strings")
if 2.0 in my_tuple:
    print(f"\"2.0\" is present in tuple \"{my_tuple}\"")

if "New" not in my_tuple:
    print(f"\"New\" not present in tuple \"{my_tuple}\"")




