'''
Accessing individual elements of a string
using negative/reverse indexing.
'''

my_string = "Accessing individual elements of a string using negative indexing"
#Accessing element at last position
#Negative indexing begins from -1
element_at_last_pos = my_string[-1]
#Accessing element at second last position
element_at_second_last_pos = my_string[-2]
#Accessing element at tenth position from end
element_at_tenth_pos_from_end = my_string[-10]

print(f"Element at first position is {element_at_last_pos}")
print(f"Element at second position is {element_at_second_last_pos}")
print(f"Element at tenth position is {element_at_tenth_pos_from_end}")


