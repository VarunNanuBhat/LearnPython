'''
Declaring a string that contains double quotes in them.
'''

#Declaring string containing double quotes with the help of single quotes
string_using_single_quotes = 'My name is "varun"'
print(string_using_single_quotes)
#Declaring string containing double quotes with the help of escape sequence
string_using_espace_sequence = "My name is \"varun\""
print(string_using_espace_sequence)

