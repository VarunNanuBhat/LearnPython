'''
Using membership operators in string
to check if a substring is present
in a string or not.
'''

my_string = "This is my string"
if "str" in my_string:
    print(f"\"str\" is present in string \"{my_string}\"")

if "New" not in my_string:
    print(f"\"New\" not present in string \"{my_string}\"")

