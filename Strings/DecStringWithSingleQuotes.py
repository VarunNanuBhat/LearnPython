'''
Declaring a string that contains single quotes in them.
'''

#Declaring string containing single quotes with the help of double quotes
string_using_double_quotes = "I'am varun"
print(string_using_double_quotes)
#Declaring string containing single quotes with the help of escape sequence
string_using_espace_sequence = 'I\'am varun'
print(string_using_espace_sequence)

