'''
List concatenation and repetition
'''

my_list_1 = [1, 2.0, 'c', "strings"]
my_list_2 = ["new", "list", "fun"]
#concatenating list
conctenated_list = my_list_1 + my_list_2
print(f"The concatenated list is \"{conctenated_list}\"")
#list repetition
repeated_list = my_list_1 * 2
print(f"The list after applying repetition is \"{repeated_list}\"")

