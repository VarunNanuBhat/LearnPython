'''
Accessing an element from a list
using its index value
'''

my_list = [1, 2.0, 'c', "strings"]
#Accessing an element at first position
element_at_first_pos = my_list[0]
#Accessing an element at second position
element_at_second_pos = my_list[1]
#Accessing the last element of a list
element_at_last_pos = my_list[-1]
#Accessing the second last element of a list
element_at_second_last_pos = my_list[-2]

print(f"Element at first position is \"{element_at_first_pos}\"")
print(f"Element at second position is \"{element_at_second_pos}\"")
print(f"Element at last position is \"{element_at_last_pos}\"")
print(f"Element at second last position is \"{element_at_second_last_pos}\"")

