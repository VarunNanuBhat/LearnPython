'''
Accessing the elements
of a dictionary using key
'''

# Creating a dictionary
my_dict = {
    "Name": "Varun",
    "Age": 23,
    "Phone Number": "9876543210"
}
# Accessing an element using key
print(f"The name of student is: {my_dict['Name']}")
print(f"The age of student is: {my_dict['Age']}")
print(f"The phone number of student is: {my_dict['Phone Number']}")




